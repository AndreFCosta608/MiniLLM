def stream_tinystories(config: PipelineConfig) -> Iterator[str]:
    """
    Iterate over the TinyStories dataset in streaming mode.

    Streaming (lazy loading) avoids downloading the entire dataset before
    starting processing. HuggingFace datasets downloads and processes
    in batches as needed.

    TinyStories has two splits: 'train' and 'validation'.
    We use only 'train' for the corpus — our model's validation split
    will be created by us.

    The relevant column is 'text', which contains the full story.

    Args:
        config: Configuration with dataset name and parameters.

    Yields:
        Individual texts from the dataset (one story at a time).
    """
    print(f"\nLoading TinyStories via streaming...")
    dataset = load_dataset(
        config.tiny_stories_name,
        split="train",
        streaming=True,     # não baixa tudo de uma vez
        trust_remote_code=False,
    )

    for example in dataset:
        text = example.get("text", "")
        if text:
            yield text


def stream_culturax_pt(config: PipelineConfig) -> Iterator[str]:
    """
    Iterate over the Portuguese subset of CulturaX in streaming mode.

    CulturaX is a large dataset (~6TB total). The Portuguese subset
    still has hundreds of GBs. Therefore:
        1. We use streaming to avoid downloading everything
        2. We stop when target_tokens_pt is reached

    The relevant column is 'text'.

    Authentication note:
        CulturaX requires accepting the terms of use on HuggingFace.
        Log in with: huggingface-cli login
        Or: from huggingface_hub import login; login()

    Args:
        config: Configuration with dataset name and language.

    Yields:
        Individual texts from the CulturaX PT subset.
    """
    print(f"\nLoading CulturaX PT via streaming...")
    print(f"  Note: requires login on HuggingFace Hub (accept terms of use)")

    dataset = load_dataset(
        config.culturax_name,
        config.culturax_lang,
        split="train",
        streaming=True,
        trust_remote_code=False,
    )

    for example in dataset:
        text = example.get("text", "")
        if text:
            yield text


