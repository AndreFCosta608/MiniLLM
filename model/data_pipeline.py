@dataclass
class PipelineConfig:
    """
    Centralized data pipeline configuration.

    Centralizing hyperparameters in a dataclass facilitates:
        - Reproducibility: save config alongside the corpus
        - Experimentation: change parameters without editing code
        - Documentation: fields describe the decisions made

    Fields:
        output_dir:        Where to save the processed corpus.
        tokenizer_path:    Path to the trained tokenizer (bpe_tokenizer).
        seq_len:           Length of each training sequence in tokens.
                           512 is a good balance for small models —
                           sufficient context without blowing up VRAM.
        target_tokens_en:  Target number of English tokens (~60% of total).
        target_tokens_pt:  Target number of Portuguese tokens (~40% of total).
        min_text_length:   Documents shorter than this are discarded.
                           Avoids degenerate sequences (loose titles, etc).
        max_text_length:   Larger documents are truncated before tokenization
                           to avoid memory spikes during processing.
        train_split:       Proportion of corpus used for training.
        val_split:         Proportion used for validation (rest goes to test).
        seed:              Seed for shuffle reproducibility.
        tiny_stories_name: Dataset name on HuggingFace Hub.
        culturax_name:     CulturaX dataset name on HuggingFace Hub.
        culturax_lang:     Language subset of CulturaX.
        batch_size:        Batch size during processing (not training).
                           Affects RAM usage during tokenization.
    """
    # Paths
    output_dir: str         = "./corpus"
    tokenizer_path: str     = "./tokenizer"

    # Sequences
    seq_len: int            = 512

    # Target volume (in tokens) — ~1GB total with vocab 16k
    # 1GB of tokenized text ≈ 250M tokens (conservative estimate)
    target_tokens_en: int   = 150_000_000   # 150M EN tokens (~60%)
    target_tokens_pt: int   = 100_000_000   # 100M PT tokens (~40%)

    # Document filtering
    min_text_length: int    = 100           # characters
    max_text_length: int    = 100_000       # characters

    # Splits
    train_split: float      = 0.95
    val_split: float        = 0.04
    # test = 1 - train - val = 0.01

    # Reproducibility
    seed: int               = 42

    # Dataset names on HuggingFace Hub
    tiny_stories_name: str  = "roneneldan/TinyStories"
    culturax_name: str      = "uonlp/CulturaX"
    culturax_lang: str      = "pt"          # Portuguese

    # Processing
    batch_size: int         = 1000          # documents per batch

    def save(self, path: str) -> None:
        """Persist the configuration to JSON for reproducibility."""
        with open(path, "w", encoding="utf-8") as f:
            json.dump(self.__dict__, f, indent=2, ensure_ascii=False)
        print(f"Configuration saved to '{path}'")

    @classmethod
    def load(cls, path: str) -> "PipelineConfig":
        """Load configuration from a JSON file."""
        with open(path, "r", encoding="utf-8") as f:
            data = json.load(f)
        config = cls()
        for key, value in data.items():
            setattr(config, key, value)
        return config


