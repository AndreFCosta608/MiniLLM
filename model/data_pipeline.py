# Utility: PyTorch-compatible DataLoader
# ─────────────────────────────────────────────────────────────

class CorpusDataset:
    """
    PyTorch dataset to load the pre-processed corpus.

    This class bridges the Arrow/Parquet corpus generated by DataPipeline
    and the PyTorch DataLoader used in the training loop.

    Implements the minimal interface required by PyTorch:
        __len__()     → number of examples
        __getitem__() → one example as a tensor

    Loading uses memory-mapping via HuggingFace datasets,
    meaning the corpus does not need to fit entirely in RAM —
    only the requested chunks are loaded.

    Uso:
        >>> from torch.utils.data import DataLoader
        >>> dataset = CorpusDataset("./corpus/train")
        >>> loader = DataLoader(dataset, batch_size=32, shuffle=True)
        >>> for batch in loader:
        ...     # batch.shape = (32, seq_len)
        ...     pass
    """

    def __init__(self, split_path: str):
        """
        Load the split from disk using memory-mapping.

        Args:
            split_path: Path to the split directory
                        (e.g., "./corpus/train").
        """
        import torch
        from datasets import load_from_disk

        self.dataset = load_from_disk(split_path)
        self.torch = torch
        print(f"CorpusDataset loaded: {len(self.dataset):,} chunks from '{split_path}'")

    def __len__(self) -> int:
        """Return the total number of chunks in the split."""
        return len(self.dataset)

    def __getitem__(self, idx: int):
        """
        Return a chunk as a PyTorch tensor.

        The chunk is returned as a LongTensor (int64), which is the type
        expected by the Transformer's nn.Embedding layer.

        Args:
            idx: Chunk index.

        Returns:
            Tensor of shape (seq_len,) with token IDs.
        """
        ids = self.dataset[idx]["input_ids"]
        return self.torch.tensor(ids, dtype=self.torch.long)


