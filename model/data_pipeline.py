# Text cleaning functions
# ─────────────────────────────────────────────────────────────

def normalize_unicode(text: str) -> str:
    """
    Normalize Unicode characters to NFC form.

    Unicode allows the same character to be represented in different ways.
    For example, "é" can be:
        - U+00E9 (é as a single character, NFC form)
        - U+0065 + U+0301 (e + combining accent, NFD form)

    Without normalization, the BPE tokenizer would treat these forms as
    different byte sequences, unnecessarily fragmenting the vocabulary.

    NFC (Canonical Decomposition followed by Canonical Composition)
    is the standard used by GPT-2, GPT-4 and most modern LLMs.

    Args:
        text: Text to normalize.

    Returns:
        NFC-normalized text.
    """
    return unicodedata.normalize("NFC", text)


def remove_html_artifacts(text: str) -> str:
    """
    Remove common HTML residues from web-extracted corpora.

    Corpora such as CulturaX are extracted from web pages and, despite
    filtering, frequently contain residual HTML tags, HTML entities
    and other artifacts that do not represent natural language.

    Removals applied:
        1. HTML tags: <div>, <p>, <br/>, etc.
        2. HTML entities: &amp; → &, &lt; → <, &nbsp; → space
        3. URLs: http://... and https://...
        4. Sequences of repeated special characters (----, ====)

    Args:
        text: Text possibly containing HTML artifacts.

    Returns:
        Cleaned text.
    """
    # Remove HTML tags
    text = re.sub(r"<[^>]+>", " ", text)

    # Decode common HTML entities
    html_entities = {
        "&amp;": "&", "&lt;": "<", "&gt;": ">",
        "&quot;": '"', "&apos;": "'", "&nbsp;": " ",
        "&#39;": "'", "&#34;": '"',
    }
    for entity, replacement in html_entities.items():
        text = text.replace(entity, replacement)

    # Remove URLs
    text = re.sub(r"https?://\S+", "", text)
    text = re.sub(r"www\.\S+", "", text)

    # Remove separator lines (---, ===, ***)
    text = re.sub(r"[-=*_]{4,}", " ", text)

    return text


def normalize_whitespace(text: str) -> str:
    """
    Normalize excessive whitespace.

    After removing HTML and URLs, text often has multiple consecutive
    spaces, tabs and excessive line breaks. This function reduces them
    to canonical forms.

    Transformations:
        - Multiple spaces → one space
        - Tabs → space
        - More than 2 consecutive line breaks → two breaks
        - Strips leading and trailing whitespace

    Args:
        text: Text with irregular whitespace.

    Returns:
        Text with normalized whitespace.
    """
    # Tabs → space
    text = text.replace("\t", " ")

    # Multiple spaces → one space
    text = re.sub(r" {2,}", " ", text)

    # More than 2 line breaks → two breaks (preserves paragraphs)
    text = re.sub(r"\n{3,}", "\n\n", text)

    return text.strip()


def is_valid_document(text: str, config: PipelineConfig) -> bool:
    """
    Check whether a document meets minimum quality criteria.

    Rejection criteria:
        1. Minimum length: very short texts do not provide enough
           context for the model to learn dependencies.
        2. Alphabetic character ratio: documents with less than
           50% letters are likely lists, tables or non-narrative content.
        3. Must have at least one non-empty line.

    Args:
        text:   Text to evaluate.
        config: Configuration with filtering thresholds.

    Returns:
        True if the document is valid, False otherwise.
    """
    # Criterion 1: minimum length
    if len(text) < config.min_text_length:
        return False

    # Criterion 2: alphabetic ratio
    # Count letters (including accented via \w) vs total characters
    alpha_count = sum(1 for c in text if c.isalpha())
    if len(text) > 0 and alpha_count / len(text) < 0.50:
        return False

    # Criterion 3: must have at least one line
    lines = [l.strip() for l in text.split("\n") if l.strip()]
    if len(lines) == 0:
        return False

    return True


def clean_document(text: str, config: PipelineConfig) -> Optional[str]:
    """
    Apply the full cleaning pipeline to a document.

    Operation order (order matters!):
        1. Unicode normalization — before anything else
        2. HTML removal — may create extra spaces
        3. Whitespace normalization — cleans up created spaces
        4. Truncation — after cleaning, to avoid truncating mid-tag
        5. Validation — decides whether the document is accepted

    Args:
        text:   Raw corpus text.
        config: Pipeline configuration.

    Returns:
        Cleaned text, or None if the document is rejected.
    """
    text = normalize_unicode(text)
    text = remove_html_artifacts(text)
    text = normalize_whitespace(text)

    # Truncate very long documents
    if len(text) > config.max_text_length:
        # Trunca no último ponto final antes do limite
        # para não cortar no meio de uma frase
        truncated = text[:config.max_text_length]
        last_period = truncated.rfind(".")
        if last_period > config.min_text_length:
            text = truncated[:last_period + 1]
        else:
            text = truncated

    if not is_valid_document(text, config):
        return None

    return text


# ─────────────────────────────────────────────────────────────
# Tokenization and chunking functions
# ─────────────────────────────────────────────────────────────

def tokenize_and_chunk(
    texts: list[str],
    tokenizer: BPETokenizer,
    seq_len: int,
) -> list[list[int]]:
    """
    Tokenize a list of texts and split into fixed-size chunks.

    This function implements the "concatenate and split" strategy,
    standard in LLM training since GPT-2:

        doc1_tokens + [SEP] + doc2_tokens + [SEP] + ...
        → split into chunks of seq_len tokens

    Why concatenate before splitting?
        - Maximizes use of available context
        - Avoids padding (filler tokens that waste model capacity)
        - The model learns to work with contexts that cross document
          boundaries (useful for long texts)

    The [SEP] separator token uses the conventional end-of-text ID.
    Since our tokenizer has no special tokens, we use byte
    0x00 (null byte), which is rarely seen in natural text.

    Args:
        texts:     List of already-cleaned texts.
        tokenizer: Trained BPETokenizer instance.
        seq_len:   Length of each chunk in tokens.

    Returns:
        List of chunks, each being a list of `seq_len` IDs.
        The last incomplete chunk is discarded to maintain fixed size.
    """
    # Separator token between documents
    # We use ID 0 (null byte) as end-of-document marker
    SEP_TOKEN = 0

    # Accumulator buffer: receives tokens from multiple documents
    buffer: list[int] = []
    chunks: list[list[int]] = []

    for text in texts:
        # Tokeniza o documento
        token_ids = tokenizer.encode(text)

        # Adiciona ao buffer com separador
        buffer.extend(token_ids)
        buffer.append(SEP_TOKEN)

        # Extract complete chunks from the buffer
        while len(buffer) >= seq_len:
            chunk = buffer[:seq_len]
            chunks.append(chunk)
            buffer = buffer[seq_len:]  # desloca o buffer

    # The remaining buffer (last incomplete chunk) is discarded
    # Alternative: padding — but padding wastes model capacity

    return chunks


