# Main pipeline
# ─────────────────────────────────────────────────────────────

class DataPipeline:
    """
    Orchestrate the full corpus preparation pipeline.

    Responsibilities:
        1. Coordinate dataset download and streaming
        2. Apply cleaning to each document
        3. Tokenize and split into fixed-size chunks
        4. Balance languages according to configuration
        5. Create train/validation/test splits
        6. Save in Arrow/Parquet format

    The pipeline is designed to be run once,
    generating the corpus to be used across multiple training experiments.

    Uso:
        >>> config = PipelineConfig()
        >>> pipeline = DataPipeline(config)
        >>> pipeline.run()
    """

    def __init__(self, config: PipelineConfig):
        """
        Initialize the pipeline.

        Args:
            config: Full pipeline configuration.
        """
        self.config = config
        self.tokenizer: Optional[BPETokenizer] = None

        # Counters for monitoring
        self.stats = {
            "en_docs_seen":      0,
            "en_docs_accepted":  0,
            "en_tokens":         0,
            "pt_docs_seen":      0,
            "pt_docs_accepted":  0,
            "pt_tokens":         0,
            "total_chunks":      0,
        }

    def _load_tokenizer(self) -> None:
        """
        Load the trained BPE tokenizer.

        The tokenizer must have been previously trained with
        bpe_tokenizer.py and saved at the configured path.

        Raises:
            FileNotFoundError: If the tokenizer is not found.
        """
        tok_path = self.config.tokenizer_path
        if not os.path.exists(tok_path):
            raise FileNotFoundError(
                f"Tokenizer not found at '{tok_path}'.\n"
                f"Run bpe_tokenizer.py first to train and save the tokenizer."
            )
        self.tokenizer = BPETokenizer.load(tok_path)

    def _process_language(
        self,
        text_iterator: Iterator[str],
        target_tokens: int,
        lang_label: str,
    ) -> list[list[int]]:
        """
        Process a stream of texts from one language until the token target is reached.

        This method is the pipeline core for each language:
            1. Consumes texts from the iterator in batches (for efficiency)
            2. Cleans each document
            3. Tokenizes and chunks the batch
            4. Stops when target_tokens is reached

        Batch processing (rather than document-by-document)
        improves tokenization efficiency, which has a per-call setup cost.

        Args:
            text_iterator: Text generator for the language.
            target_tokens: Target number of tokens for this language.
            lang_label:    Label for logs ("EN" or "PT").

        Returns:
            List of tokenized chunks (each chunk = list of IDs).
        """
        all_chunks: list[list[int]] = []
        total_tokens = 0
        docs_seen = 0
        docs_accepted = 0

        # Buffer de documentos limpos para tokenização em lote
        clean_buffer: list[str] = []

        pbar = tqdm(
            desc=f"Processing {lang_label}",
            unit=" tokens",
            total=target_tokens,
        )

        for raw_text in text_iterator:
            docs_seen += 1

            # Clean the document
            clean_text = clean_document(raw_text, self.config)
            if clean_text is None:
                continue  # document rejected

            docs_accepted += 1
            clean_buffer.append(clean_text)

            # Process in batches for efficiency
            if len(clean_buffer) >= self.config.batch_size:
                chunks = tokenize_and_chunk(
                    clean_buffer,
                    self.tokenizer,
                    self.config.seq_len,
                )
                all_chunks.extend(chunks)
                batch_tokens = len(chunks) * self.config.seq_len
                total_tokens += batch_tokens
                pbar.update(batch_tokens)
                clean_buffer = []

                # Stop when target is reached
                if total_tokens >= target_tokens:
                    break

        # Process the remaining buffer
        if clean_buffer and total_tokens < target_tokens:
            chunks = tokenize_and_chunk(
                clean_buffer,
                self.tokenizer,
                self.config.seq_len,
            )
            all_chunks.extend(chunks)
            total_tokens += len(chunks) * self.config.seq_len
            pbar.update(len(chunks) * self.config.seq_len)

        pbar.close()

        # Update global statistics
        self.stats[f"{lang_label.lower()}_docs_seen"]     = docs_seen
        self.stats[f"{lang_label.lower()}_docs_accepted"] = docs_accepted
        self.stats[f"{lang_label.lower()}_tokens"]        = total_tokens

        acceptance_rate = docs_accepted / max(docs_seen, 1) * 100
        print(f"\n  {lang_label}: {docs_accepted}/{docs_seen} docs accepted "
              f"({acceptance_rate:.1f}%) | {total_tokens:,} tokens | "
              f"{len(all_chunks):,} chunks")

        return all_chunks

    def _create_splits(
        self,
        chunks: list[list[int]],
    ) -> tuple[list[list[int]], list[list[int]], list[list[int]]]:
        """
        Split chunks into train, validation and test sets.

        Shuffling before splitting is crucial: without it, chunks from
        one language would be concentrated in one split, and the model
        would not see both languages during validation.

        Proportions (configurable in PipelineConfig):
            - Train:      95% of chunks
            - Validation:  4% of chunks
            - Test:        1% of chunks

        The test set is intentionally small — it serves only
        to report final perplexity after full training.
        It must never be seen during model development.

        Args:
            chunks: Full list of chunks (all languages, shuffled).

        Returns:
            Tuple (train_chunks, val_chunks, test_chunks).
        """
        random.seed(self.config.seed)
        random.shuffle(chunks)

        n = len(chunks)
        n_train = int(n * self.config.train_split)
        n_val   = int(n * self.config.val_split)

        train_chunks = chunks[:n_train]
        val_chunks   = chunks[n_train:n_train + n_val]
        test_chunks  = chunks[n_train + n_val:]

        print(f"\n  Splits created:")
        print(f"    Train      : {len(train_chunks):>10,} chunks")
        print(f"    Validation : {len(val_chunks):>10,} chunks")
        print(f"    Test       : {len(test_chunks):>10,} chunks")

        return train_chunks, val_chunks, test_chunks

    def _save_split(
        self,
        chunks: list[list[int]],
        split_name: str,
    ) -> None:
        """
        Save a corpus split in Arrow/Parquet format.

        Storage format:
            An Arrow table with a single column: "input_ids"
            Each row is a list of `seq_len` integers.

            Example (seq_len=4, for illustration):
                input_ids
                [256, 312, 89, 1024]
                [1024, 45, 789, 256]
                ...

        Why Arrow/Parquet?
            - Columnar reading: the DataLoader reads only "input_ids",
              without loading metadata or other columns
            - Memory-mapping: the OS manages the cache,
              allowing datasets larger than available RAM
            - Snappy compression (Parquet default): reduces size ~30-50%
              with negligible decompression cost

        Args:
            chunks:     List of chunks to save.
            split_name: Split name ("train", "val", "test").
        """
        output_path = os.path.join(self.config.output_dir, split_name)
        os.makedirs(output_path, exist_ok=True)

        # Convert to HuggingFace dataset
        dataset = Dataset.from_dict({"input_ids": chunks})

        # Save in Arrow format (used internally by HF datasets)
        dataset.save_to_disk(output_path)

        # Also save a Parquet copy for interoperability
        parquet_path = os.path.join(output_path, f"{split_name}.parquet")
        table = pa.table({"input_ids": pa.array(chunks, type=pa.list_(pa.int32()))})
        pq.write_table(
            table,
            parquet_path,
            compression="snappy",   # good compression/speed ratio
        )

        # Calculate on-disk size
        size_mb = os.path.getsize(parquet_path) / (1024 ** 2)
        print(f"    {split_name:<10}: {len(chunks):>10,} chunks → "
              f"{size_mb:.1f} MB ({parquet_path})")

    def print_stats(self) -> None:
        """
        Print a summary of pipeline statistics.

        Useful for checking:
            - Document acceptance rate (indicates filter quality)
            - Actual EN/PT ratio (may differ from target if a corpus is smaller)
            - Total volume of data generated
        """
        s = self.stats
        total_tokens = s["en_tokens"] + s["pt_tokens"]
        total_chunks = s["total_chunks"]

        en_pct = s["en_tokens"] / max(total_tokens, 1) * 100
        pt_pct = s["pt_tokens"] / max(total_tokens, 1) * 100

        print(f"\n{'='*55}")
        print(f"  Pipeline Statistics")
        print(f"{'='*55}")
        print(f"  English (TinyStories):")
        print(f"    Docs processed   : {s['en_docs_seen']:>12,}")
        print(f"    Docs accepted    : {s['en_docs_accepted']:>12,}")
        print(f"    Tokens generated : {s['en_tokens']:>12,}  ({en_pct:.1f}%)")
        print(f"\n  Portuguese (CulturaX):")
        print(f"    Docs processed   : {s['pt_docs_seen']:>12,}")
        print(f"    Docs accepted    : {s['pt_docs_accepted']:>12,}")
        print(f"    Tokens generated : {s['pt_tokens']:>12,}  ({pt_pct:.1f}%)")
        print(f"\n  Total:")
        print(f"    Tokens           : {total_tokens:>12,}")
        print(f"    Chunks (seq={self.config.seq_len}) : {total_chunks:>12,}")
        print(f"{'='*55}\n")

    def run(self) -> None:
        """
        Run the complete pipeline from start to finish.

        Steps:
            1. Load the trained BPE tokenizer
            2. Process TinyStories (EN) until target_tokens_en is reached
            3. Process CulturaX PT until target_tokens_pt is reached
            4. Combine and shuffle all chunks
            5. Create train/val/test splits
            6. Save in Arrow/Parquet
            7. Save configuration and statistics for reproducibility
        """
        print("=" * 55)
        print("  DataPipeline — Bilingual PT+EN Corpus Preparation")
        print("=" * 55)

        # ── Step 1: Tokenizer ────────────────────────────────────────────
        print("\n[1/5] Loading tokenizer...")
        self._load_tokenizer()

        # ── Etapa 2: TinyStories (EN) ────────────────────────────────────
        print("\n[2/5] Processing TinyStories (EN)...")
        en_chunks = self._process_language(
            text_iterator=stream_tinystories(self.config),
            target_tokens=self.config.target_tokens_en,
            lang_label="EN",
        )

        # ── Etapa 3: CulturaX PT ─────────────────────────────────────────
        print("\n[3/5] Processing CulturaX PT...")
        pt_chunks = self._process_language(
            text_iterator=stream_culturax_pt(self.config),
            target_tokens=self.config.target_tokens_pt,
            lang_label="PT",
        )

        # ── Etapa 4: Combina e embaralha ─────────────────────────────────
        print("\n[4/5] Combining and shuffling corpus...")
        all_chunks = en_chunks + pt_chunks
        self.stats["total_chunks"] = len(all_chunks)
        print(f"  Total chunks before split: {len(all_chunks):,}")

        train_chunks, val_chunks, test_chunks = self._create_splits(all_chunks)

        # ── Step 5: Save ─────────────────────────────────────────────────
        print("\n[5/5] Saving corpus in Arrow/Parquet...")
        os.makedirs(self.config.output_dir, exist_ok=True)
        self._save_split(train_chunks, "train")
        self._save_split(val_chunks,   "val")
        self._save_split(test_chunks,  "test")

        # Save configuration and stats for reproducibility
        self.config.save(
            os.path.join(self.config.output_dir, "pipeline_config.json")
        )
        with open(
            os.path.join(self.config.output_dir, "pipeline_stats.json"),
            "w", encoding="utf-8"
        ) as f:
            json.dump(self.stats, f, indent=2)

        self.print_stats()
        print(f"Corpus saved to '{self.config.output_dir}/'")
        print("Pipeline completed successfully.")


